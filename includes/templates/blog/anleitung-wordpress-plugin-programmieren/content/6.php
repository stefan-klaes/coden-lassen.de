<h3>Schritt 6: Plugin installieren</h3>
<p>
Super, geschafft! Zippe den Plugin Ordner und navigiere im WordPress Backend zu "Plugins" > "installieren" > 
"Plugin hochladen" und lade deinen Zip Ordner hoch. Klicke auf "jetzt installieren" und du hast es geschafft.
</p>

