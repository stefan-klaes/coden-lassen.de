<h3>Fazit zu WordPress Shortcodes.</h3>
<p>
Die Erstellung individueller Shortcodes ist für mehrere Anwendungsfälle sinnvoll. In diesem Beispiel haben wir uns 
mit der Angabe des Alters beschäftigt. Dies ist jedoch nur ein Anwendungsfall von vielen. Man kann mit Shortcodes 
viel Arbeit ersparen und zudem sind die Angaben aktuell und es ist ein wirklich smarter Weg um selbst PHP Code 
in WordPress einzubinden.
