<h3>Was ist ein WordPress Shortcode?</h3>
<p>
    Ein WordPress Shortcode bietet die Möglichkeit sehr einfach dynamische Inhalte in einer Website einzubauen. Der Inhalt 
    kann in WordPress Editorn in dieser Form eingebunden werden: <span class="bold colprim">[dein_shortcode]</span>
</p>
<p>
    Wenn man individuelle WordPress Shortcodes erstellt, kann man dynamische Inhalte per PHP bereitstellen. Das gibt 
    unzählige Möglichkeiten. Ich werde folgend einige Beispiele erklären.
</p>
