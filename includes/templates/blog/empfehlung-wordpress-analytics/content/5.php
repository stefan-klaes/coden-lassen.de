<h3>Woocommerce: Mehr Insights zu deinen Verkäufen</h3>

<p>
Das Woocommerce Tracking ist serverseitig integriert und misst immer dann, wenn eine neue Bestellung ausgelöst wird. Simplest 
Analytics bereitet die Daten im Backenend visuell auf, sodass man u.A. folgendes analytieren kann:
</p>

<ul>
    <li>Von welchen Seiten kamen die Käufer auf deine Website</li>
    <li>Haben die Käufer deine definierten Tracking URLs vor einem Kauf aufgerufen</li>
    <li>Wann wurde gekauft zu welchen Werten</li>
</ul>

