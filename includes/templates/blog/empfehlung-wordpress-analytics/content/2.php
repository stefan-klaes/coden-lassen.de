<h3>Welche Tools können diese Anforderungen erfüllen?</h3>

<p>
Die Anforderungen erfüllen tatsächlich sehr viele Tools. Im professionellen Bereich wird sehr häufig Google Analytics verwendet 
und aus meiner Sicht sind die Funktionen und die Bandbreite an Möglichkeiten von Google Analyitics schwer zu schlagen. Für 
mich hat sich jedoch herausgestellt, dass im Wordpress und Woocommerce Bereich nur die wenigsten Websitenbetreiber wirklich 
dieses Spektrum benötigen oder gar ausschöpfen können. Daher habe ich ein eigenes Plugin entwickelt, dass sich ganz stark 
auf ein Kerntracking fokussiert und die Daten auf dem eigenen Server speichert - also dem gleichen Server, der auch die 
Datenbank für WordPress enthält.
</p>

