<h3>Warum sollte ich Webanalytics für meine WordPress Website nutzen?</h3>
<p>
Webanalytics Tools bieten sich an um tiefere Einblicke in die Nutzung der Website zu erhalten. Wie viele Aufrufe haben die 
Unterseiten, wie viele Besucher haben die Seiten, woher stammt der Traffic meiner Website und viele weitere Fragen lassen 
sich mit Webanalytics Tools beantworten. Zudem kann es sehr interessant sein zu sehen worauf die Nutzer klicken oder welche 
sogenannten Tracking-URLs sie aufrufen (z.B. www.coden-lassen.de?campaign=test-kampagne). Wer einen Woocommerce Shop 
hat, kann zudem tiefere Einblicke in die Herkunft der Verkäufe erhalten.
</p>

<p>
In kurz:
</p>
<ul>
    <li>Aufrufe</li>
    <li>Besucher</li>
    <li>Referrer (Google, Facebook, Foren, usw.)</li>
    <li>Tracking URLs (z.B. www.coden-lassen.de?campaign=test-kampagne)</li>
    <li>Events (Klicks, Formular abgesendet, usw.)</li>
    <li>Woocommerce Sales Insights</li>
</ul>



