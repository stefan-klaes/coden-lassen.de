<h3>Zusammenfassung</h3>

<ul>
    <li>WordPress bietet viele Funktionen, die die Sicherheit erhöhen</li>
    <li>Alle Daten immer prüfen</li>
    <li>Den WordPress Code Standard lesen und einhalten</li>
</ul>

<p>
Selbstverständlich sind das nur ein paar wenige Beispiele für die Einhaltung des korrekten Code Standards. Es gibt sehr viele 
weitere Beispiele und Funktionen, die WordPress bereitstellt bzw. die man selbst nutzen sollte. Ebenso sind bestimmte 
Programmierweisen empfehlenswert wie das Benutzen von PHP Klassen, gute Benennungen von Funktionen, Reduzierung von 
unnötigen Code und vieles mehr. Ich möchte hier ausdrücklich erwähnen, dass nicht jedes nicht Einhalten des Code 
Standards automatisch einen Hackingangriff ermöglicht. Aber wer es kann, sollte aus meiner Sicht den Standard einhalten 
und so die Sicherheit erhöhen.
</p>